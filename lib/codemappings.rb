  def CodeMappings.feature_type_name(feature_type_code)
    return @device_type[feature_type_code]
  end

  def CodeMappings.feature_name(feature_type_code, feature_code)
    return @type_to_feature_names[feature_type_code].fetch(feature_code, "unknown")
  end

  def CodeMappings.bus_type(bus_type_code)
    return @bus[bus_type_code]
  end
  
