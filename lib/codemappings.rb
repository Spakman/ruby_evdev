# generated by manually munging /usr/include/linux/input.h for its #defines so
# it's not necessarily up to date or complete.
#
# [device_type] mapping device type codes to names.  Use feature_type_name for
#               simple remap rather than accessing the variable directly
# [bus]         mapping bus type codes to names.  You should just use the
#               EventDevice#bus_type_name
# [sync, keycode, button, etc..] All the rest are name mappings for the various
#                                features of a specific type
